a="Good"
for i in a:
	print(i) 

for i in range(len(a)):
	print(i)			#prints after finding length of variable a
	
for i in range(1,10):
	print(i)			#prints starting from 1 to 9 exits at 10
	
for i in range(1,11,2):
	print(i)			#prints starting from 1 to 9 even numbers exits at 11

