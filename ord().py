
# ********  ord()

# ********  ord(ch) function returns the Unicode code from a given character. 
# ********  ord() accepts only one character as argument

print(ord('z'))  # Output: 122
print(ord('£'))  # Output: 163
print(ord('2'))  # Output: 50
print(ord('g'))  # Output: 103
print(ord('&'))  # Output: 38


value = ord('€')
print(value)     # Output: 8364
