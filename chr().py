
# ********  chr()

# ********  chr(num) function returns the Unicode code from a given number. 
# ********  chr() accepts only number as argument

print(chr(122))  # Output: z
print(chr(163))  # Output: £
print(chr(50))   # Output: 2
print(chr(103))  # Output: g
print(chr(38))   # Output: &


value = chr(8364)
print(value)     # Output: €
